package com.javaex.ex04;

public class Ex01 {

	public static void main(String[] args) {
		//초기화, 조건식, 증감식
		
		
		
		
		int i = 0; //초기값
		
		while(i<=5)  {   //조건식
			System.out.println("I like java"+i);

			
			i++;

		}
		
		
		//반복구간 값
		//i=0 <-- 초기값 , 0<5 <-- 조건문 , 0++ 증감식 0-->1
		//i=1 , 1<5 <-- 조건문 , 1++ 증감식 1-->2
		//i=2 , 2<5 <-- 조건문 , 2++ 증감식 2-->3
		//i=3 , 3<5 <-- 조건문 , 3++ 증감식 3-->4
		//i=4 , 4<5 <-- 조건문 , 4++ 증감식 4-->5
		//i=5 , 5<5 --> false
		
	}

}
