package com.javaex.ex04;

public class Ex05 {

	public static void main(String[] args) {
		
		
		System.out.println("구구단 출력");
		

		
		for(int dan=2; dan<=9; dan++ ) { 
			
		
			for(int j=1; j<=9; j++) {
				
				System.out.println(dan+"*"+j + "=" + (dan*j));
				
			}
			
		}


	}

}
